# Simple calculation using user input
# Demonstrates how to take numbers from the user and add them

# Convert input to integer using int()
num1 = int(input("Enter first number: "))   # First number
num2 = int(input("Enter second number: "))  # Second number

# Perform addition
sum_result = num1 + num2  # Add the two numbers

# Print the result
print("The sum is:", sum_result)
