# Data Types in Python
# Python has several basic data types: int, float, string, boolean

number = 10          # Integer: whole numbers
price = 99.99        # Float: decimal numbers
text = "Python"      # String: text inside quotes
status = True        # Boolean: True or False

# Print values and their data types
print(number, "is of type", type(number))  # number stores an integer
print(price, "is of type", type(price))    # price stores a float
print(text, "is of type", type(text))      # text stores a string
print(status, "is of type", type(status))  # status stores a boolean

