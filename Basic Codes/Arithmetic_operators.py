# Demonstration of arithmetic operators in Python

a = 10  # 'a' stores 10 (integer)
b = 3   # 'b' stores 3 (integer)

# Addition
print("Addition:", a + b)        # Adds a and b → 10 + 3 = 13

# Subtraction
print("Subtraction:", a - b)     # Subtracts b from a → 10 - 3 = 7

# Multiplication
print("Multiplication:", a * b)  # Multiplies a and b → 10 * 3 = 30

# Division
print("Division:", a / b)        # Divides a by b → 10 / 3 = 3.333... (float result)

# Modulus
print("Modulus:", a % b)         # Remainder of a divided by b → 10 % 3 = 1

